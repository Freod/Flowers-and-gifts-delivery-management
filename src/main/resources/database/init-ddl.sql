CREATE TABLE IF NOT EXISTS USERS
(
    ID        SERIAL,
    EMAIL     VARCHAR(50) UNIQUE,
    PASSWORD  VARCHAR(128),
    FIRSTNAME VARCHAR(50),
    LASTNAME  VARCHAR(50),
    ROLE      VARCHAR(50),
    ACTIVE    BOOLEAN NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE IF NOT EXISTS PRODUCTS
(
    ID     SERIAL,
    NAME   VARCHAR(50) UNIQUE,
    PRICE  INTEGER,
    IMAGE  VARCHAR(128),
    ACTIVE BOOLEAN NOT NULL,
    PRIMARY KEY (ID)
);

